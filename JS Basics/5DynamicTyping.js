// JavaScript is a Dynamically Typed Language:

let name = "Tayyab"; // String literal
let age = 19; // Number literal
let isGraduated = false; //  or true Boolean literal
let firstName = undefined; // undefined literal
let selectedColor = null; // null/object literal
 
console.log(typeof(name));
console.log(typeof(age));
console.log(typeof(isGraduated));
console.log(typeof(firstName));
console.log(typeof(selectedColor));

name = 20;
age = "Tayyab";
isGraduated = undefined;

console.log();
console.log(typeof(name));
console.log(typeof(age));
console.log(typeof(isGraduated));